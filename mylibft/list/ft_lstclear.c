#include "../header/libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*temp;

	while (*lst)
	{
		temp = *lst;
		temp = temp->next;
		ft_lstdelone(*lst, del);
		*lst = temp;
	}
}
