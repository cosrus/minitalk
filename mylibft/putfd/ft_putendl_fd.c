#include "../header/libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	size_t	p;

	p = 0;
	while (s[p])
	{		
		write(fd, &s[p], 1);
		p++;
	}
	write(fd, "\n", 1);
}
