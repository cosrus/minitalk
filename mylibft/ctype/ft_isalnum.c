#include "../header/libft.h"

int	ft_isalnum(int c)
{
	if (c >= '0' && c <= 'z')
	{
		if ((c > '9' && c < 'A') || (c > 'Z' && c < 'a'))
			return (0);
		else
			return (1);
	}
	else
		return (0);
}
